let currentLanguage = localStorage.getItem('mb_language') || 'en';

function humanizeKey(key) {
  return key.replace(/_/g, ' ').replace(/\b\w/g, char => char.toUpperCase());
}

function t(key) {
  if (!key) return '';
  const lang = i18n[currentLanguage] || i18n.en;
  return lang[key] || i18n.en[key] || humanizeKey(key);
}

function updateStaticDOM() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (key) el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (key) el.placeholder = t(key);
  });
}

function changeLanguage(lang) {
  if (!i18n[lang]) return;
  currentLanguage = lang;
  localStorage.setItem('mb_language', lang);

  document.querySelectorAll('.lang-select').forEach(select => {
    select.value = lang;
  });
  updateStaticDOM();

  const appShell = document.getElementById('app-shell');
  if (appShell && appShell.classList.contains('active')) {
    if (typeof buildSidebarNav === 'function') buildSidebarNav(currentRole);
    if (typeof navigateTo === 'function' && currentPanel) navigateTo(currentPanel);
  }
  if (window.updateThemeToggle) window.updateThemeToggle();
}

document.addEventListener('DOMContentLoaded', () => {
  document.querySelectorAll('.lang-select').forEach(select => {
    select.value = currentLanguage;
    select.addEventListener('change', e => changeLanguage(e.target.value));
  });
  updateStaticDOM();
});
